    private void jButtonCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCreateActionPerformed
        try {
            stmt = con.createStatement();
            String query = "INSERT INTO categoria (nom, descripcio) "
                    + "VALUES ('" + jTextFieldNom.getText() + "', "
                    + "'" + jTextFieldDescripcio.getText()+ "')";
            stmt.executeUpdate(query);
            JOptionPane.showMessageDialog(this, "Categoria inserida correctament", "Status", JOptionPane.INFORMATION_MESSAGE);
            dispose();
            Categoria mainWindow = new Categoria(con);   // Create a new MainJFrame window
        mainWindow.setVisible(true); 
        } catch (Exception e) {
            String txt = "Error al inserir categoria: " + e;
            JOptionPane.showMessageDialog(this, txt, "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButtonCreateActionPerformed

