    private void jButtonUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonUpdateActionPerformed
        try {
           stmt = con.createStatement();
           String query = "UPDATE treballador SET nss = '"+jTextFieldNSS.getText()+"', "
                    + "dni = '"+ jTextFieldDNI.getText() +"', "
                    + "nom = '"+ jTextFieldNom.getText() +"', "
                    + "data_incorporacio = '"+ jTextFieldNom.getText() +"', "
                    + "WHERE nss ='"+ nss +"'";
            
            
            //System.out.println(query);
            stmt.executeUpdate(query);
            JOptionPane.showMessageDialog(this, "Client modificat correctament", "Status", JOptionPane.INFORMATION_MESSAGE);
            dispose();
            Clients obj = new Clients(con);
            obj.setVisible(true);
        } catch (Exception e) {
            String txt = "Error al modificar client: " + e;
            JOptionPane.showMessageDialog(this, txt, "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButtonUpdateActionPerformed
    private void plenarCamps(String nss) {
        try {
                String query = "SELECT * FROM treballador WHERE nss = '"+ nss +"'";
                stmt = con.createStatement();
                rs = stmt.executeQuery(query);
                while (rs.next()) {
                    jTextFieldNSS.setText(rs.getString("nss"));
                    jTextFieldDNI.setText(rs.getString("dni"));
                    jTextFieldNom.setText(rs.getString("nom"));
                    jTextFieldData.setText(rs.getString("data_incorporacio"));
                    
                }
            } catch (Exception e) {

                JOptionPane.showMessageDialog(this, e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);

            }
    }
