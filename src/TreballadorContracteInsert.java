    private void jButtonCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCreateActionPerformed
        try {
            stmt = con.createStatement();
            String query = "INSERT INTO contracte (nss_treballador,data_inici, data_final, import_anual) "
                    + "VALUES ('" + NSS + "', "
                    + "'" + jTextFieldData_Inici.getText() + "', "
                    + "'" + jTextFieldData_Fina.getText() + "', "
                    + "'" + jTextFieldImport_Anual.getText()+"')";
            
            
            System.out.println(query);
            stmt.executeUpdate(query);
            JOptionPane.showMessageDialog(this, "Contracte inserit correctament", "Status", JOptionPane.INFORMATION_MESSAGE);
            dispose();
            ContractesTreballador mainWindow = new ContractesTreballador(con,NSS);   // Create a new MainJFrame window
        mainWindow.setVisible(true); 
        } catch (Exception e) {
            String txt = "Error al inserir contracte: " + e;
            JOptionPane.showMessageDialog(this, txt, "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButtonCreateActionPerformed

